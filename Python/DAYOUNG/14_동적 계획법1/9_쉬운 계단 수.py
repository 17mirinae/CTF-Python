#시간초과
import sys
n = int(sys.stdin.readline())
end = 10 ** n
start = 10 ** (n-1)

count = 0
for i in range(start, end):
    if i < 10:
        count += 1
    else:
        string = str(i)
        st = 1
        for k in range(len(string)-1):
            if abs(int(string[k]) - int(string[k+1])) ==1 :
                st += 1
        if st == len(string):
            count += 1

print(count)

